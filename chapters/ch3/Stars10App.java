package chapters.ch3;

/**
 * Prints 10 stars horizontally.
 *
 * @author kyria
 */
public class Stars10App {
    public static void main(String[] args) {
        // Init
        int i = 1;

        // Loop
        while (i <= 10){
            System.out.print("*");
            i++;
        }
    }
}
